import gsap from "gsap";
import { useGSAP } from "@gsap/react";
import { ScrollTrigger } from "gsap/all";

import AnimatedTitle from "./AnimatedTitle";

gsap.registerPlugin(ScrollTrigger);

const About = () => {
  useGSAP(() => {
    const clipAnimation = gsap.timeline({
      scrollTrigger: {
        trigger: "#clip",
        start: "center center",
        end: "+=800 center",
        scrub: 0.5,
        pin: true,
        pinSpacing: true,
      },
    });

    clipAnimation.to(".mask-clip-path", {
      width: "100vw",
      height: "100vh",
      borderRadius: 0,
    });
  });

  return (
    <div id="about" className="min-h-screen w-screen">
      <div className="relative mb-8 mt-36 flex flex-col items-center gap-5">
        <p className="font-general text-sm uppercase md:text-[10px]">
          Welcome to nerdwork
        </p>

        <AnimatedTitle
          title="Enter the ultimate Nerdverse: <br/> comics, conventions, and the biggest geek community."
          containerClass="mt-5 !text-blue-600 text-center !text-3xl"
        />

        <div className="about-subtext">
          <p>
            {" "}
            Attend the most exciting comic conventions Connect with one of the
            largest nerd community
          </p>
          <p className="text-gray-500">
            nerdwork unites people from all over the country from countless
            platforms, both digital and physical, into a unified Nerd community
          </p>
        </div>
      </div>

      <div className="h-dvh w-screen" id="clip">
        <div className="mask-clip-path about-image">
          <img
            src="img/comic5.jpg"
            alt="Background"
            className="absolute left-0 top-0 size-full object-cover"
          />
        </div>
      </div>
    </div>
  );
};

export default About;
